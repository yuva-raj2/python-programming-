import math 
pi=3.14
a=4
b=5
c=2
#abs is used to print the absolute value if the given value is negative it will print only as the positive value
print(abs(pi))
#ceil will not take the decimal and it will ignore the decimals and move to the next number
print(math.ceil(pi))
#floor will ignore the decimal and print the values before the decimal only
print(math.floor(pi))
#round will round the given number
print(round(pi))
#pow will multiply the times of the given number (pi,4) here it will be in the form of pi*pi*pi*pi for 4 times and it will display the result
print(pow(pi,4))
#sqrt will square the given number
print(math.sqrt(35.33002))
#max will check the maximum value of 3 or more variable and print the maximum value of them
print(max(a,b,c))
#min will check the minimum value of 3 or more variable and print the minimum value of  them
print(min(a,b,c))
